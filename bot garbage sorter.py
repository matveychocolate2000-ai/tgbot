import telebot
from telebot import types

bot = telebot.TeleBot("TOKEN")

@bot.message_handler(commands=['start', 'hello'])
def send_welcome(message):

    markup = types.ReplyKeyboardMarkup(resize_keyboard=True)

    btn1 = types.KeyboardButton("♻️ Что куда сдавать?")
    btn2 = types.KeyboardButton("📜 Правила")

    markup.add(btn1, btn2)

    welcome_text = f'Привет! Я помогу тебе быстро разобраться с мусором в быту. Нажми кнопку ниже или просто напиши мне название предмета (например: бутылка, чек, крышка), и я скажу, что с ним делать! {bot.get_me().first_name}!'
    bot.send_message(message.chat.id, welcome_text, reply_markup=markup)

@bot.message_handler(content_types=['text'])
def handle_text(message):
    text = message.text.lower()

    if message.text == "♻️ Что куда сдавать?":
        bot.send_message(message.chat.id, "Просто напиши мне название предмета (например: батарейка, чек, градусник).")
        
    elif message.text == "📜 Правила":
        rules = (
            "1. Сполосни и сожми (чтобы не пахло и занимало меньше места).\n\n"
            "2. Обращай внимание на маркировку (цифры в треугольниках).\n\n"
            "3. Разделяй: пластик отдельно, бумагу отдельно."
        )
        bot.send_message(message.chat.id, rules)
        
    elif "батарейк" in text:
        bot.send_message(message.chat.id, "❌ **Опасные отходы!** Батарейки нельзя кидать в общий мусор. Сдай в специальный бокс в магазине (есть во ВкусВилле, Эльдорадо и др.).")

    elif "бутылк" in text:
        bot.send_message(message.chat.id, "♻️ **Пластик (01 PET).** Самый перерабатываемый пластик! Сполосни, сожми (чтобы выпустить воздух) и бросай в желтый бак.")

    elif "чек" in text:
        bot.send_message(message.chat.id, "🗑️ **Общий мусор.** Чеки сделаны из термобумаги, в них почти нет целлюлозы, зато полно токсичного бисфенола. В макулатуру их нельзя!")

    elif "крышк" in text:
        bot.send_message(message.chat.id, "♻️ **Пластик (02 HDPE).** Собирай отдельно! Во многих городах есть проект «Добрые крышечки» — деньги от их переработки идут на благотворительность.")

    elif "коробк" in text or "картон" in text:
        bot.send_message(message.chat.id, "♻️ **Макулатура.** Сними пластиковый скотч, сожми коробку плоской и сдай в синий бак. Важно: коробки от пиццы с жирными пятнами сдавать нельзя — только в общий мусор!")

    elif "банк" in text or "жест" in text:
        bot.send_message(message.chat.id, "♻️ **Металл.** Алюминиевые банки от газировки или жестяные от консервов отлично перерабатываются бесконечное число раз. Сполосни и сожми.")

    elif "стекл" in text or ("бутыл" in text and "стекл" in text):
        bot.send_message(message.chat.id, "♻️ **Стекло.** Банки и бутылки из-под напитков можно сдавать в специальный бак. Лампочки и оконные стекла туда кидать нельзя!")

    elif "пакет" in text or "пленк" in text:
        bot.send_message(message.chat.id, "⚠️ **Смотри маркировку.** Обычные прозрачные фасовочные пакеты и стрейч-пленку (04 LDPE) принимают во многих эко-центрах. Пакеты-майки из супермаркетов переработать сложнее.")

    elif "одежд" in text or "вещ" in text:
        bot.send_message(message.chat.id, "👕 **Вторая жизнь.** Хорошую одежду лучше отдать в благотворительные фонды или продать. Ветошь (рваную одежду) принимают некоторые бренды на переработку. В обычный бак не бросай.")

    elif "градусник" in text or "ртут" in text:
        bot.send_message(message.chat.id, "🚨 **СВЕРХОПАСНО!** Ртутные градусники и люминесцентные лампы содержат пары ртути. Их нужно сдавать строго в специальные пункты утилизации МЧС или городские эко-боксы.")

    elif "стакан" in text:
        bot.send_message(message.chat.id, "☕ **Общий мусор.** «Бумажные» стаканчики для кофе внутри покрыты слоем пластиковой пленки, чтобы не размокать. Разделить их невероятно сложно, поэтому их почти нигде не принимают.")

    elif "огрызок" in text or "еда" in text or "очистки" in text:
        bot.send_message(message.chat.id, "🍎 **Органика.** Пищевые отходы лучше всего отправлять в домашний компостер или измельчитель (диспоузер). В обычном пакете на свалке они гниют и выделяют метан.")

    else:
        bot.send_message(message.chat.id, "🤔 Я пока не знаю этот предмет. Но помни главное правило: чистый пластик, макулатуру, стекло и металл почти всегда можно сдать на переработку!")

bot.polling(none_stop=True)
